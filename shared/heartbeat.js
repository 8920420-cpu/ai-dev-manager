// RUNNER-HEARTBEAT-001 — liveness-сигнал хостовых раннеров для вотчдога свежести
// (scripts/ensure-fresh-runners.ps1).
//
// Зачем: вотчдог рестартит демон только если он (а) не запущен или (б) его процесс
// старше кода. «Живой, но завис» (процесс есть, код свежий, но цикл не двигается —
// напр. claim() навсегда завис на полуоткрытом сокете) этими проверками не ловится.
// Раннер на КАЖДОЙ итерации своего рабочего цикла зовёт beat() — пишет текущее время
// в файл. Вотчдог считает демон зависшим и точечно перезапускает, если mtime файла
// старше порога (RUNNER_HEARTBEAT_STALE_MIN, дефолт 30 мин), ХОТЯ процесс жив.
//
// Почему beat НА ВЕРХУШКЕ итерации, а не по таймеру: сигнал должен означать «прогресс
// есть» (итерация завершилась), а не «event-loop жив» — иначе зависший на await claim
// раннер продолжал бы «биться» по setInterval. Долгая ЛЕГИТИМНАЯ задача (Architect до
// 20 мин) на время держит beat — поэтому порог вотчдога заведомо больше максимального
// таймаута задачи (30 мин > 20 мин), ложных срабатываний нет.
//
// Путь файла раннер получает из RUNNER_HEARTBEAT_FILE (ставит start-runners.ps1:
// logs/<name>.heartbeat). Механизм НЕОБЯЗАТЕЛЕН: без переменной beat() — no-op, и
// вотчдог полагается только на «не запущен»/«старше кода» (обратная совместимость —
// старый раннер без heartbeat не даёт ложных рестартов, т.к. файла просто нет).
import { writeFileSync } from 'node:fs';

const HEARTBEAT_FILE = String(process.env.RUNNER_HEARTBEAT_FILE || '').trim();

// Лучшая попытка записать метку живости. Ошибки (гонка одновременной записи из
// нескольких воркеров на Windows, отсутствующий каталог) ГЛОТАЕМ — liveness-сигнал
// никогда не должен ронять рабочий цикл раннера.
export function beat() {
  if (!HEARTBEAT_FILE) return;
  try {
    writeFileSync(HEARTBEAT_FILE, String(Date.now()));
  } catch {
    /* best-effort: mtime важнее содержимого, следующий тик перезапишет */
  }
}

// Включён ли heartbeat в этом процессе (задан ли путь). Для логов/тестов.
export function heartbeatEnabled() {
  return Boolean(HEARTBEAT_FILE);
}
