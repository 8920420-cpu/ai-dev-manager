/**
 * Состояние раскрытия дерева раздела «Задачи». Ключи раскрытых узлов: `p:<id>`
 * для проектов и `t:<id>` для задач. При повторном обновлении дерева (после
 * продвижения, ручного перемещения, перезапуска или события оркестратора) нельзя
 * пересобирать набор с нуля — иначе у пользователя схлопываются раскрытые задачи
 * и подзадачи. Эта чистая функция инкапсулирует правило: на первой загрузке
 * раскрываем все проекты, на последующих обновлениях сохраняем выбор пользователя.
 */
export function projectKey(id: string): string {
  return `p:${id}`;
}

export function taskKey(id: string): string {
  return `t:${id}`;
}

/**
 * Рассчитать набор раскрытых узлов при загрузке/обновлении дерева.
 * - initial=true (первая загрузка): раскрываем все проекты, чтобы дерево
 *   открывалось наполненным;
 * - initial=false (обновление после мутации/события): сохраняем текущее
 *   раскрытие пользователя как есть (включая раскрытые задачи `t:`).
 */
export function expandedForLoad(
  prev: Set<string>,
  projectIds: string[],
  initial: boolean,
): Set<string> {
  if (!initial) return prev;
  return new Set(projectIds.map(projectKey));
}
