-- =====================================================================
-- Папка с задачами проекта: projects.tasks_path.
-- Подпапка 2-го уровня внутри папки проекта (root_path), где лежат задачи.
-- Именно за этой папкой следит Scanner и принимает оттуда новые задачи
-- (раньше эту роль играла docs_path; теперь docs_path — чисто описательная
-- «карта» проекта, а приём задач идёт из tasks_path).
-- Абсолютный путь; NULL — папка не задана. Существование каталога проверяет
-- scanner-service. Идемпотентная миграция (повторный запуск безопасен).
-- =====================================================================
BEGIN;

ALTER TABLE projects ADD COLUMN IF NOT EXISTS tasks_path text;
COMMENT ON COLUMN projects.tasks_path IS
  'Абсолютный путь к папке задач проекта. Подставляется в watch_directory Scanner-этапа (приём задач). При NULL Scanner откатывается на docs_path.';

COMMIT;
