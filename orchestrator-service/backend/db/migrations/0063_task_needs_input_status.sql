-- =====================================================================
-- TASK-NEEDS-INPUT-001 (шаг 1/2) — статус «нужна доп. информация».
-- =====================================================================
-- Исполнитель роли, упёршийся в неоднозначность задачи, раньше мог только
-- выдумать ответ или вернуть success=false (задача уходила в requeue и, после
-- нескольких кругов, в BLOCKED — без единого понятного человеку вопроса).
-- Теперь он останавливается и СПРАШИВАЕТ: задача паркуется в NEEDS_INPUT с
-- вопросом и (опционально) вариантами ответа, человек отвечает через интерфейс,
-- ответ уезжает в контекст следующего прогона.
--
-- Статус нетерминальный и НЕ клеймится ролями (как WAITING_FOR_CHILDREN), но
-- смысл другой: тот ждёт подзадачи на барьере join, этот — ответа ЧЕЛОВЕКА.
--
-- ВНИМАНИЕ: ALTER TYPE ... ADD VALUE нельзя выполнять внутри той же транзакции,
-- в которой новое значение затем используется. Поэтому значение enum добавляем
-- ОТДЕЛЬНЫМ файлом и БЕЗ обрамляющего BEGIN/COMMIT (колонки — в 0064).
-- Идемпотентна (IF NOT EXISTS).
-- =====================================================================

ALTER TYPE task_status ADD VALUE IF NOT EXISTS 'NEEDS_INPUT';
