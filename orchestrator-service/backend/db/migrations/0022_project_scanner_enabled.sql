-- =====================================================================
-- SCANNER-TOGGLE-001 — переключатель приёма задач Scanner на уровне проекта.
-- Идемпотентная миграция (повторный запуск безопасен).
-- =====================================================================
-- Если scanner_enabled = true, Scanner следит за «папкой документов» проекта
-- (projects.docs_path) и автоматически принимает оттуда задачи (создаёт их в БД
-- под ролью Приёмщика задач). Если false — Scanner проект не отслеживает.
-- По умолчанию выключено: владелец проекта включает приём явно на карточке.
-- =====================================================================

BEGIN;

ALTER TABLE projects ADD COLUMN IF NOT EXISTS scanner_enabled boolean NOT NULL DEFAULT false;
COMMENT ON COLUMN projects.scanner_enabled IS
  'Включён ли автоматический приём задач Scanner из папки документов проекта.';

COMMIT;
