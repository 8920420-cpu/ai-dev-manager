-- =====================================================================
-- FORK-JOIN-001 (Phase 4/5) — рантайм параллельных веток.
-- Идемпотентная миграция (повторный запуск безопасен).
-- =====================================================================
-- Задача в граф-схеме (со связями) отслеживает СВОЙ текущий узел по стабильному
-- ключу current_stage_key — это снимает неоднозначность «по какому узлу идёт
-- задача», когда одна роль встречается в нескольких ветках. Для линейных схем
-- (без рёбер) поле остаётся NULL и используется прежняя маршрутизация по позиции.
-- =====================================================================

BEGIN;

ALTER TABLE tasks ADD COLUMN IF NOT EXISTS current_stage_key uuid;
COMMENT ON COLUMN tasks.current_stage_key IS
  'Стабильный ключ текущего узла блок-схемы (project_stages.stage_key). NULL — линейная маршрутизация по позиции.';

CREATE INDEX IF NOT EXISTS idx_tasks_current_stage_key ON tasks(current_stage_key);

COMMIT;
