-- =====================================================================
-- FORK-JOIN-001 (Phase 1) — статус ожидания подзадач на барьере join.
-- =====================================================================
-- Родитель, расщеплённый узлом fork на параллельные подзадачи, паркуется в
-- статусе WAITING_FOR_CHILDREN до тех пор, пока все дети не станут терминальными
-- (узел join снимает барьер). Статус нетерминальный и не клеймится ролями.
--
-- ВНИМАНИЕ: ALTER TYPE ... ADD VALUE нельзя выполнять внутри той же транзакции,
-- в которой новое значение затем используется. Поэтому миграция — ОТДЕЛЬНЫМ
-- файлом и БЕЗ обрамляющего BEGIN/COMMIT. Идемпотентна (IF NOT EXISTS).
-- =====================================================================

ALTER TYPE task_status ADD VALUE IF NOT EXISTS 'WAITING_FOR_CHILDREN';
