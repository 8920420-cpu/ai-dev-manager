-- SCANNER-STAGE-STATUS-001 — статус задач, c которым работает этап Scanner.
-- Этап Scanner теперь привязывается к одному task_status: исполнитель ищет
-- ТОЛЬКО задачи этого статуса. Это позволяет ставить несколько этапов Scanner
-- на разные статусы; ограничение «один Scanner на проект» снимается и заменяется
-- требованием уникальности статуса среди включённых Scanner-этапов (валидация
-- на уровне приложения, stages.js).
--
-- Колонка nullable: не-Scanner этапы и отключённые Scanner без статуса допустимы.
--
-- Read-only аудит перед миграцией (2026-06-23):
--   project_stages: task_status — новая колонка, существующих данных нет.
-- Существующие строки НЕ изменяются (колонка добавляется как NULL).
--
-- Rollback: ALTER TABLE project_stages DROP COLUMN task_status;
-- Идемпотентно: ADD COLUMN IF NOT EXISTS.
-- =====================================================================
BEGIN;

ALTER TABLE project_stages ADD COLUMN IF NOT EXISTS task_status task_status;

COMMENT ON COLUMN project_stages.task_status IS
  'Статус задач, c которым работает этап Scanner (роль SCANNER). Исполнитель '
  'забирает только задачи этого статуса. NULL для не-Scanner и отключённых '
  'этапов. Среди включённых Scanner-этапов проекта статус должен быть уникален.';

COMMIT;
