-- =====================================================================
-- PROGRAMMER-PRIORITY-001 (отмена) — вернуть параллелизм программиста до 3.
-- =====================================================================
-- Ранее решение PROGRAMMER-PRIORITY-001 зажало programmer_concurrency в 1
-- (спека и БД). Отменяем: возвращаем worktree-параллелизм до 3 агентов по
-- РАЗНЫМ сервисам (изначальный cap по PROGRAMMER-WORKTREE-PER-SERVICE, 0032).
-- Поднимаем сохранённое значение с 1 до 3 ТОЛЬКО если оно ровно 1 — чтобы не
-- перетереть ручную настройку другим значением (напр. 2). Если строки нет,
-- дефолт 3 подставит appSettings.js.
-- =====================================================================
BEGIN;

UPDATE app_settings
   SET value = '3'::jsonb, updated_at = now()
 WHERE key = 'programmer_concurrency'
   AND value = '1'::jsonb;

COMMIT;
