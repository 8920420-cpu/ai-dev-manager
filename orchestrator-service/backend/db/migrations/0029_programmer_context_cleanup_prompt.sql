-- =====================================================================
-- PROGRAMMER-CONTEXT-CLEANUP-001 — явное требование очистки контекста
-- после сдачи задачи программистом.
--
-- Идемпотентно: блок добавляется только в prompt роли PROGRAMMER и только
-- если там ещё нет требования про очистку рабочего контекста.
-- =====================================================================
BEGIN;

UPDATE roles
   SET prompt = replace(
     prompt,
     '   После подачи сигнала не правь код, тесты или поля задачи.',
     '   После подачи сигнала не правь код, тесты или поля задачи.' || E'\n' ||
     '   После успешной сдачи результата очисти рабочий контекст сессии программиста' || E'\n' ||
     '   (например, командой `/clear` в Claude Code), чтобы следующая задача не получила' || E'\n' ||
     '   остатки контекста выполненной задачи.'
   )
 WHERE code = 'PROGRAMMER'
   AND prompt IS NOT NULL
   AND prompt LIKE '%После подачи сигнала не правь код, тесты или поля задачи.%'
   AND prompt NOT LIKE '%очисти рабочий контекст сессии программиста%';

COMMIT;
