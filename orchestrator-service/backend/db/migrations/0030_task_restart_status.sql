-- =====================================================================
-- TASK-RESTART-001 — статус «Перезапуск» для зависших задач.
-- =====================================================================
-- Задачи в статусе BACKLOG берутся Приёмщиком задач (TASK_INTAKE_OFFICER)
-- только если у проекта есть включённый этап с маппингом роли на этот статус
-- (или у проекта вообще нет настроенного маршрута — канонический фолбэк). Если
-- вход проекта — READY, задачи в BACKLOG зависают навсегда. Кнопка «Перезапустить»
-- в разделе «Задачи» переводит такие зависшие задачи в статус RESTART, под которым
-- Приёмщик задач забирает их БЕЗУСЛОВНО (см. claimLlmRoleTask) и пускает по маршруту.
--
-- ALTER TYPE ... ADD VALUE нельзя выполнять внутри транзакционного блока в
-- старых версиях Postgres, поэтому файл содержит ровно один statement без BEGIN/COMMIT.
-- =====================================================================
ALTER TYPE task_status ADD VALUE IF NOT EXISTS 'RESTART';
