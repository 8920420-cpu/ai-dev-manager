-- =====================================================================
-- TASK-AUTO-ACCEPT-001 — авто-приёмка DONE по умолчанию ВЫКЛЮЧЕНА.
-- =====================================================================
-- Фиксируем явное значение auto_accept_done=false в app_settings, чтобы
-- поведение не зависело от дефолтов кода при будущих изменениях: свежие
-- DONE-задачи ждут ручной приёмки в подразделе «Задачи → Проверка».
-- Идемпотентно (ON CONFLICT DO NOTHING) — уже выставленное значение (в т.ч.
-- включённая вручную авто-приёмка) НЕ перетирается. Ретро-приёмку не трогаем:
-- accepted_at существующих DONE-задач остаётся как есть.
-- =====================================================================
BEGIN;

INSERT INTO app_settings (key, value)
VALUES ('auto_accept_done', 'false'::jsonb)
ON CONFLICT (key) DO NOTHING;

COMMIT;
