-- =====================================================================
-- PIPELINE-DYNAMIC-ROUTE-002 — нейтрализация маршрутизации в промтах ролей.
-- Роль больше НЕ знает, от кого принимает и кому передаёт задачу: маршрут
-- определяется порядком этапов проекта (project_stages). Поэтому из промтов
-- убираем поле `next_role` и явные упоминания соседних ролей в «передаче».
--
-- Подход — аддитивный и щадящий правки пользователя:
--   1) regexp_replace снимает строки `next_role: ...` из блоков «Формат результата»;
--   2) точечные replace() убирают известные фразы-маршрутизации канонических ролей;
--   3) каждому промту один раз дописывается единый блок «## Маршрут» с описанием
--      абстрактного исхода (его проверяем через NOT LIKE, чтобы не дублировать).
--
-- Read-only аудит: меняется только roles.prompt (текст). Идемпотентно: повторный
-- запуск не добавляет блок повторно и не находит уже удалённых строк.
-- Rollback: повторно применить 0016 (восстановит канонический текст там, где он пуст).
-- =====================================================================
BEGIN;

-- 1) Снять строку next_role из всех промтов (YAML и JSON-варианты).
UPDATE roles
   SET prompt = regexp_replace(prompt, '[ \t]*"?next_?[rR]ole"?[ \t]*[:=][^\n]*\n?', '', 'g')
 WHERE prompt IS NOT NULL AND prompt ~ 'next_?[rR]ole';

-- 2) Точечно нейтрализовать известные фразы-маршрутизации канонических ролей.
UPDATE roles SET prompt = replace(prompt,
  'При `READY` передай результат Decomposer. При существенной неопределённости верни `BLOCKED`, не подменяя решение догадкой.',
  'При `READY` задача идёт дальше по маршруту проекта. При существенной неопределённости верни `BLOCKED`, не подменяя решение догадкой.'
) WHERE code = 'ARCHITECT';

UPDATE roles SET prompt = replace(prompt,
  'Если план противоречив или неполон, верни его Architect. Иначе передай первую готовую задачу Programmer —',
  'Если план противоречив или неполон, верни `BLOCKED` (задача уйдёт на доработку выше по маршруту). Иначе подготовь первую готовую задачу для следующего этапа —'
) WHERE code = 'DECOMPOSER';

UPDATE roles SET prompt = replace(prompt,
  '`APPROVED` ведёт в Pipeline Service, `NEEDS_FIX` — обратно Programmer, архитектурное противоречие — Architect.',
  '`APPROVED` пропускает задачу дальше по маршруту; `NEEDS_FIX`/`REJECTED` отправляет её на доработку.'
) WHERE code = 'TASK_REVIEWER';

UPDATE roles SET prompt = replace(prompt,
  'При успехе переходи к Documentation Auditor. При любой ошибке выполнения — только к Failure Analyst.',
  'При успехе задача идёт дальше по маршруту; при любой ошибке выполнения — на анализ причины.'
) WHERE code = 'PIPELINE_SERVICE';

UPDATE roles SET prompt = replace(prompt,
  'Для исправимого дефекта передай задачу Programmer. Инфраструктурную ошибку можно вернуть Pipeline Service только после устранения внешней причины.',
  'Для исправимого дефекта верни задачу на доработку (`DIAGNOSED`). Неустранимую инфраструктурную/неопределённую причину помечай `INFRASTRUCTURE_BLOCKED`/`INCONCLUSIVE` — задача останавливается.'
) WHERE code = 'FAILURE_ANALYST';

UPDATE roles SET prompt = replace(prompt,
  'При `NO_CHANGES` переходи к Git Integrator. При `UPDATE_REQUIRED` — к Documentation Keeper. Существенное незапланированное архитектурное изменение верни Architect.',
  'Верни статус: `NO_CHANGES` — документы актуальны (задача идёт дальше); `UPDATE_REQUIRED` — нужно обновить документы; `ARCHITECT_REVIEW_REQUIRED` — незапланированное архитектурное изменение. Маршрут по статусу выбирает оркестратор.'
) WHERE code = 'DOCUMENTATION_AUDITOR';

UPDATE roles SET prompt = replace(prompt,
  'После успешного обновления передай изменения Git Integrator. При противоречивом задании верни Documentation Auditor.',
  'После успешного обновления (`UPDATED`) задача идёт дальше по маршруту. При противоречивом задании верни `BLOCKED`.'
) WHERE code = 'DOCUMENTATION_KEEPER';

UPDATE roles SET prompt = replace(prompt,
  'Следующей ролью после завершения Programmer назначать `TASK_REVIEWER`.',
  'Передавать завершённую задачу в БД — дальше её ведёт цепочка этапов проекта.'
) WHERE code = 'SCANNER';
UPDATE roles SET prompt = replace(prompt,
  'Импортированная задача заходит на `TASK_REVIEWER`,\n   дальше — обычная цепочка ролей.',
  'Импортированная задача входит в маршрут проекта с его первого активного этапа.'
) WHERE code = 'SCANNER';

UPDATE roles SET prompt = replace(prompt,
  'ревью/тесты/коммит ведёт цепочка ролей в БД (`TASK_REVIEWER` → … → `DONE`), не файлы.',
  'дальнейшие этапы (ревью/тесты/коммит) ведёт маршрут проекта в БД, не файлы.'
) WHERE code = 'PROGRAMMER';

UPDATE roles SET prompt = replace(prompt,
  '4. Сформировать сводный результат и передать управление следующей роли (`SCANNER`).',
  '4. Сформировать сводный результат — дальше задачу ведёт маршрут проекта.'
) WHERE code = 'STRUCTURE_KEEPER';
UPDATE roles SET prompt = replace(prompt,
  'При `READY` передай управление `SCANNER`. При неустранимой из контекста проблеме (неизвестная миграция,',
  'При `READY` задача идёт дальше по маршруту проекта. При неустранимой из контекста проблеме (неизвестная миграция,'
) WHERE code = 'STRUCTURE_KEEPER';

-- 3) Единый блок про маршрут — дописываем один раз (если ещё не дописан).
UPDATE roles
   SET prompt = prompt || E'\n\n## Маршрут\n\nТы не знаешь и не указываешь, от кого принимаешь и кому передаёшь задачу — это определяет порядок этапов проекта. Возвращай только свой статус-исход: успех — задача идёт дальше; провал/доработка — назад по маршруту; `BLOCKED` — остановка до вмешательства.'
 WHERE prompt IS NOT NULL
   AND prompt <> ''
   AND prompt NOT LIKE '%## Маршрут%';

COMMIT;
