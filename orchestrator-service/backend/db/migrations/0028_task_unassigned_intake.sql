-- =====================================================================
-- TASK-INTAKE-UNASSIGNED-001 — «корзина неразобранных» Приёмщика задач.
-- =====================================================================
-- Постановщик задачи через MCP явно сообщает папку проекта (projectPath).
-- Backend детерминированно сопоставляет её с зарегистрированным проектом.
-- Если сопоставить не удалось, задача всё равно создаётся — но БЕЗ проекта
-- (project_id IS NULL) и паркуется в статусе BLOCKED. Такие задачи попадают в
-- «Неразобранные» у роли Task Intake Officer; после ручного назначения проекта
-- они получают project_id и уходят по цепочке ролей дальше.
-- =====================================================================
BEGIN;

-- Разрешаем задачам существовать без проекта (неразобранные).
ALTER TABLE tasks ALTER COLUMN project_id DROP NOT NULL;

-- Идемпотентность интейка для неразобранных: UNIQUE (project_id, external_id)
-- не ловит дубли при NULL project_id (в SQL NULL != NULL), поэтому отдельный
-- частичный уникальный индекс по external_id среди неразобранных задач.
CREATE UNIQUE INDEX IF NOT EXISTS uniq_tasks_unassigned_external
  ON tasks (external_id)
  WHERE project_id IS NULL AND external_id IS NOT NULL;

COMMIT;
